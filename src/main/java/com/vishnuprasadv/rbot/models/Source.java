package com.vishnuprasadv.rbot.models;

public class Source {
	private int height;

	private String url;

	private int width;

	public int getHeight() {
		return this.height;
	}

	public String getUrl() {
		return this.url;
	}

	public int getWidth() {
		return this.width;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public void setWidth(int width) {
		this.width = width;
	}
}